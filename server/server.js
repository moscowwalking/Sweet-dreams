function formatDateLocal(d) {
  const pad = n => (n < 10 ? '0' + n : n);
  return (
    d.getFullYear().toString() +
    pad(d.getMonth() + 1) +
    pad(d.getDate()) +
    'T' +
    pad(d.getHours()) +
    pad(d.getMinutes()) +
    pad(d.getSeconds())
  );
}

app.post('/send-invite', async (req, res) => {
  try {
    console.log('üì® /send-invite request body:', req.body);

    const { city, place, date, timeStart, timeEnd, email } = req.body;
    if (!city || !place || !date || !timeStart || !timeEnd) {
      return res.status(400).json({ error: '–ù–µ–æ–±—Ö–æ–¥–∏–º—ã –ø–æ–ª—è: city, place, date, timeStart, timeEnd' });
    }

    const recipientEmails = [
      email?.trim() || process.env.TO_EMAIL,
      'oda2002@mail.ru'
    ];

    const [year, month, day] = date.split('-').map(Number);
    const [startHour, startMinute] = timeStart.split(':').map(Number);
    const [endHour, endMinute] = timeEnd.split(':').map(Number);

    const start = new Date(year, month - 1, day, startHour, startMinute);
    const end = new Date(year, month - 1, day, endHour, endMinute);

    const icsString = `BEGIN:VCALENDAR
VERSION:2.0
CALSCALE:GREGORIAN
METHOD:REQUEST
BEGIN:VEVENT
UID:${Date.now()}@sweet-dreams
DTSTAMP:${formatDateLocal(new Date())}
DTSTART;TZID=Europe/Moscow:${formatDateLocal(start)}
DTEND;TZID=Europe/Moscow:${formatDateLocal(end)}
SUMMARY:üíñ –í—Å—Ç—Ä–µ—á–∞
DESCRIPTION:–°–∫–æ—Ä–æ —É–≤–∏–¥–∏–º—Å—è! ${city}, ${place}.
LOCATION:${place}, ${city}
STATUS:CONFIRMED
SEQUENCE:0
TRANSP:OPAQUE
END:VEVENT
END:VCALENDAR`;

    const payload = {
      api_key: process.env.UNISENDER_API_KEY,
      message: {
        recipients: recipientEmails.map(email => ({ email })),
        subject: `üíå –í—Å—Ç—Ä–µ—á–∞: ${city}, ${place}`,
        from_email: process.env.FROM_EMAIL,
        from_name: 'Sweet Dreams',
        body: { html: `<p>–°–∫–æ—Ä–æ —É–≤–∏–¥–∏–º—Å—è –≤ <b>${city}</b>!<br>üìç ${place}<br>üìÖ ${date}<br>‚è∞ ${timeStart}‚Äì${timeEnd}</p>` },
        attachments: [{ type: 'text/calendar', name: 'invite.ics', content: Buffer.from(icsString).toString('base64') }]
      }
    };

    const response = await fetch('https://go2.unisender.ru/ru/transactional/api/v1/email/send.json', {
      method: 'POST',
      headers: {
        'Authorization': `Bearer ${process.env.UNISENDER_API_KEY}`,
        'Content-Type': 'application/json'
      },
      body: JSON.stringify(payload)
    });

    const data = await response.json();
    if (response.ok && !data.error) {
      console.log('‚úÖ Invite sent successfully');
      res.json({ success: true });
    } else {
      console.error('‚ùå UniSender error:', data);
      res.status(500).json({ error: data.error?.message || '–û—à–∏–±–∫–∞ UniSender', data });
    }
  } catch (err) {
    console.error('‚ùå /send-invite error:', err.stack || err);
    res.status(500).json({ error: '–û—à–∏–±–∫–∞ —Å–µ—Ä–≤–µ—Ä–∞: ' + err.message, stack: err.stack });
  }
});

function cleanPlacesJson() {
  try {
    const path = './places.json';
    if (!fs.existsSync(path)) return;

    const raw = fs.readFileSync(path, 'utf8');
    const data = JSON.parse(raw);
    if (!Array.isArray(data)) return;

    const before = data.length;
    const cleaned = data.filter(p => p.origUrl || p.thumbUrl);
    if (cleaned.length !== before) {
      fs.writeFileSync(path, JSON.stringify(cleaned, null, 2));
      console.log(`üßπ –û—á–∏—â–µ–Ω places.json: —É–¥–∞–ª–µ–Ω–æ ${before - cleaned.length} –ø—É—Å—Ç—ã—Ö –∑–∞–ø–∏—Å–µ–π`);
    }
  } catch (err) {
    console.error('‚ùå –û—à–∏–±–∫–∞ –æ—á–∏—Å—Ç–∫–∏ places.json:', err);
  }
}

