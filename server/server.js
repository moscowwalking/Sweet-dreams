// --- –í–æ—Å—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–∏–µ places.json –∏–∑ S3 ---
async function restorePlacesFromS3() {
  try {
    const data = await s3.getObject({ Bucket: BUCKET_NAME, Key: 'backups/places.json' }).promise();
    if (data.Body) {
      fs.writeFileSync(PLACES_FILE, data.Body);
      console.log(`‚úÖ places.json restored from S3 (${JSON.parse(data.Body).length} items)`);
    }
  } catch (err) {
    console.log('‚ö†Ô∏è No backup found in S3, starting with empty list');
    if (fs.existsSync(PLACES_FILE)) fs.unlinkSync(PLACES_FILE);
  }
}

app.post('/update-caption', async (req, res) => {
  try {
    const { coords, photoIndex = 0, caption } = req.body;
    console.log('üì• –ü–æ–ª—É—á–µ–Ω –∑–∞–ø—Ä–æ—Å –Ω–∞ –æ–±–Ω–æ–≤–ª–µ–Ω–∏–µ –ø–æ–¥–ø–∏—Å–∏:', { coords, photoIndex, caption });

    // –ó–∞–≥—Ä—É–∂–∞–µ–º —Ç–µ–∫—É—â–∏–π places.json –∏–∑ –±–∞–∫–µ—Ç–∞ (AWS SDK v2 —Å—Ç–∏–ª—å)
    const fileData = await s3.getObject({
      Bucket: BUCKET_NAME,
      Key: 'backups/places.json'
    }).promise();

    const places = JSON.parse(fileData.Body.toString());

    // –ù–∞—Ö–æ–¥–∏–º –Ω—É–∂–Ω–æ–µ –º–µ—Å—Ç–æ –ø–æ –∫–æ–æ—Ä–¥–∏–Ω–∞—Ç–∞–º
    const foundIndex = places.findIndex(p => 
      p.coords && 
      Array.isArray(p.coords) &&
      p.coords.length === 2 &&
      Math.abs(p.coords[0] - coords[0]) < 0.0001 &&
      Math.abs(p.coords[1] - coords[1]) < 0.0001
    );

    if (foundIndex === -1) {
      console.warn('‚ö†Ô∏è –ú–µ—Å—Ç–æ –Ω–µ –Ω–∞–π–¥–µ–Ω–æ –¥–ª—è –∫–æ–æ—Ä–¥–∏–Ω–∞—Ç:', coords);
      return res.status(404).json({ success: false, error: '–ú–µ—Å—Ç–æ –Ω–µ –Ω–∞–π–¥–µ–Ω–æ' });
    }

    const found = places[foundIndex];
    
    // –û–±–Ω–æ–≤–ª—è–µ–º –ø–æ–¥–ø–∏—Å—å
    if (found.photos && Array.isArray(found.photos) && found.photos[photoIndex]) {
      found.photos[photoIndex].caption = caption;
    } else {
      found.caption = caption;
    }

    // –°–æ—Ö—Ä–∞–Ω—è–µ–º –æ–±—Ä–∞—Ç–Ω–æ (AWS SDK v2 —Å—Ç–∏–ª—å)
    await s3.putObject({
      Bucket: BUCKET_NAME,
      Key: 'backups/places.json',
      Body: JSON.stringify(places, null, 2),
      ContentType: 'application/json'
    }).promise();

    console.log(`‚úÖ –ü–æ–¥–ø–∏—Å—å —Å–æ—Ö—Ä–∞–Ω–µ–Ω–∞ —É –º–µ—Å—Ç–∞ [${coords}]`);
    res.json({ success: true });
  } catch (err) {
    console.error('‚ùå –û—à–∏–±–∫–∞ –ø—Ä–∏ –æ–±–Ω–æ–≤–ª–µ–Ω–∏–∏ –ø–æ–¥–ø–∏—Å–∏:', err);
    res.status(500).json({ success: false, error: err.message });
  }
});

